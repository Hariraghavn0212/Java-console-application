public class Evaluvate {
    static double addition(double val1, double val2)
    {
        return val1+val2;
    }
    static double addition (double val1,double val2,double val3)
    {
        return val1+val2+val3;
    }
    static double subtraction(double val1, double val2)
    {
        return val1-val2;
    }
    static double subtraction (double val1,double val2,double val3)
    {
        return val1-val2-val3;
    }
    static double multiplication(double val1, double val2)
    {
        return val1*val2;
    }
    static double multiplication (double val1,double val2,double val3)
    {
        return val1*val2*val3;
    }
    static double division(double val1, double val2)
    {
        return val1/val2;
    }
    static double division (double val1,double val2,double val3)
    {
        return val1/val2/val3;
    }
}